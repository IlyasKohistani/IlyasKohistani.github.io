var lang_setter = $("#lang");

lang_setter.click(function () {
  const lang_text = lang_setter.text();
  const cur_lang = langs[lang_text];
  for (id in cur_lang) {
    $(`#${id},[data-id="${id}"]`).text(cur_lang[id]);
  }
  lang_setter.attr("href", "#" + lang_text);
});

$(document).ready(function () {
  $.get(
    "https://api.ipdata.co?api-key=test",
    function (response) {
      if (response.country_name === "Turkey") {
        lang_setter.click();
      }
    },
    "jsonp"
  );
});
